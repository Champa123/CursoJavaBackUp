
public class Calculadora {

	public void Suma(int numero, int numero1){
		int resultado =numero+numero1;
		System.out.println(resultado);
	}
	
	public void resta(int numero, int numero1){
		int resultado=(numero-numero1);
		System.out.println(resultado);
	}

	public void multiplicacion(int numero, int numero1){
		int resultado = numero*numero1;
		System.out.println(resultado);
	}
	
	public void division( int dividendo, int divisor){
		int resultado=dividendo/divisor;
		System.out.println(resultado);
	}
	
}
