
public class Calculadora2 {

	private int numero;
	private int numeroa;
	
	int sumar ( int numero, int numeroa){
		return (numero+numeroa);
	}
	int restar ( int numero, int numeroa){
		return (numero-numeroa);
	}
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public int getNumeroa() {
		return numeroa;
	}
	public void setNumeroa(int numeroa) {
		this.numeroa = numeroa;
	}
}
